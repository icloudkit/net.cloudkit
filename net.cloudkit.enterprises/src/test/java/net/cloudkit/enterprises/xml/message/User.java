package net.cloudkit.enterprises.xml.message;

import net.cloudkit.enterprises.xml.message.annotation.Element;
import net.cloudkit.enterprises.xml.message.annotation.Format;

@Element(name = "USER")
public class User {

	private long id;

	@Element(name = "NAME", format = @Format(pattern = ""))
	private String name;

	@Element(name = "AGE")
	private int age;

	@Element(name = "PASSWORD")
	private String password;

	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the age
	 */
	public int getAge() {
		return age;
	}

	/**
	 * @param age the age to set
	 */
	public void setAge(int age) {
		this.age = age;
	}

	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}

	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}

	@Override
	public String toString() {
		return "";
	}

}
